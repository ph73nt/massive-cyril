package couk.nucmedone.massivecyril.shared.labtest.exceptions;

public class LowSampleNumberException extends Exception {

	public LowSampleNumberException() {
		super();
	}

	public LowSampleNumberException(String message) {
		super(message);
	}

	public LowSampleNumberException(Throwable cause) {
		super(cause);
	}

	public LowSampleNumberException(String message, Throwable cause) {
		super(message, cause);
	}

}
