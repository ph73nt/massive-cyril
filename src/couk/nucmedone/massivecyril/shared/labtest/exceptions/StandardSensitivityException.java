package couk.nucmedone.massivecyril.shared.labtest.exceptions;

public class StandardSensitivityException extends Exception {

	public StandardSensitivityException() {
		super();
	}

	public StandardSensitivityException(String message) {
		super(message);
	}

	public StandardSensitivityException(Throwable cause) {
		super(cause);
	}

	public StandardSensitivityException(String message, Throwable cause) {
		super(message, cause);
	}

}
